package JavaProgram;

public class Assign1 {
	
// Program for Solid Half Diamond
	
	public static void main(String[] args) {
		int i,j;
		for(i=0;i<5;i++) // 0 1 2 3 4
		{
			for(j=0;j<=i;j++) //0 1 2 3 4 
			{
				System.out.print(0 + " ");	
				
			}
		System.out.println();	
		}
		
		for(i=4;i>0;i--) // 
		{
			for(j=0;j<i;j++) //0 
			{
				System.out.print(0 + " ");				
			}
		System.out.println();	
		}
	
	}

}
